#!/bin/bash

# ==============================================================================
# 配置区域（无需修改，保持默认即可）
# ==============================================================================
# 输出/读取的文件名（固定为deepseek回答.md）
OUTPUT_FILE="deepseek回答.md"
# AI模型与系统提示
AI_MODEL="deepseek-v4-pro"
SYSTEM_PROMPT="完成要求规定的任务。"
# ==============================================================================

# ==============================================================================
# 1. 命令行参数校验 + 读取上下文文件
# ==============================================================================
# 检查是否输入了提问内容
if [ $# -eq 0 ]; then
    echo -e "❌ 用法：bash $0 \"你的提问内容\""
    echo -e "示例：bash $0 基于上文内容，帮我优化这段文字"
    exit 1
fi

# 读取命令行输入的提问
USER_QUESTION="$*"

# 读取deepseek回答.md的内容（作为上下文，文件不存在则为空）
FILE_CONTEXT=""
if [ -f "$OUTPUT_FILE" ]; then
    FILE_CONTEXT=$(cat "$OUTPUT_FILE")
fi

# 拼接：上下文（文件内容）+ 你的提问
if [ -n "$FILE_CONTEXT" ]; then
    CONTENT="上下文内容：
$FILE_CONTEXT

我的提问：$USER_QUESTION"
else
    CONTENT="$USER_QUESTION"
fi

# ==============================================================================
# 2. API配置（优先使用环境变量的API_KEY）
# ==============================================================================
API_KEY="${API_KEY:-sk-3b227102823d48d499796908610a3e54}"

if [ -z "$API_KEY" ]; then
    echo "错误：未设置API_KEY，请在终端执行 export API_KEY=\"你的密钥\" 后重试"
    exit 1
fi

# ==============================================================================
# 3. 构造请求参数
# ==============================================================================
JSON_PAYLOAD=$(jq -n \
  --arg model "$AI_MODEL" \
  --arg system "$SYSTEM_PROMPT" \
  --arg user "$CONTENT" \
  '{
    model: $model,
    messages: [
      {role: "system", content: $system},
      {role: "user", content: $user}
    ],
    stream: false
  }')

# ==============================================================================
# 4. 发送请求 + 输出结果（终端+文件）
# ==============================================================================
echo "🔍 正在请求 DeepSeek API..."
echo "----------------------------------------"

# 调用API，解析结果，同时输出到终端和文件
curl -s https://api.deepseek.com/chat/completions \
  -H "Content-Type: application/json" \
  -H "Authorization: Bearer $API_KEY" \
  -d "$JSON_PAYLOAD" | jq -r '.choices[0].message.content' | tee "$OUTPUT_FILE"

# 提示完成
echo -e "\n========================================"
echo "✅ 回答已保存到：$OUTPUT_FILE"
echo "========================================"